package sony_interview;

public class Swap_to_numbers_without_using_third_variable {

	public static void main(String[] args) {
		// Without using third variable
		int a= 10;
		int b= 20;
		b=b-a;
		a=a+b;
		System.out.println("The value present in a is : "+a);
		System.out.println("The value present in b is : "+b);
		
	}

}
